package Abstraction;

public class AbstractBasic {
	public static void main(String[] args) {
		/*Abstract class:
		 * 1-Class level
		 * 2-Method Level
		 * 
		 * 3-Abstract class cannot be instantiated
		 * 4-Abstract class can contain:
		 * 		->Only abstract methods
		 * 		->only non abstract methods
		 * 		->abstract + non abstract methods
		 * 5-If class is abstract then it is not fixed that the methods will be abstract.
		 * 6-If method is abstract then class **must** be abstract.
		 * 7-Only abstract method declaration no definition(no body of the method).
		 * 8-abstract methods must be overriden in the subclass.
		 * 9-Final + Abstract ->cannot be used at class level.
		 * 10-Final + Abstract ->cannot be used at method level.
		 * 11-Private + Abstract ->cannot be used at same time.
		 * 12-Static + Abstract ->cannot be used at class level.
		 * 13-Static + Abstract ->cannot be used at method level.
		 * 14-Abstract class can be used by extending it (IS-A Inheritance)
		 * 		-> It **must** override all the unimplemented methods of the abstract class.
		 * 15-Non Abstract methods inside an Abstract class must be defined(method body).
		 * 16-Abstract class can use HAS-A relation.
		 * 		->Java Anonymous inner class
		 * 			->Java anonymous inner class is an inner class without a name and for which only a single object is created.
		 * 			->In simple words, a class that has no name is known as an anonymous inner class in Java
		 * 			->A class is created, but its name is decided by the compiler, **which extends the abstract base class** and provides the implementation of the abstract method.
		 * 			->An object of the Anonymous class is created that is referred to by abstract base class object/reference variable of abstract base class.
		 * 			
		 * */		
	}

}
